export function getAllSearchableContent(): { id: string; title: string; text: string; type: 'intro' | 'meeting' }[] {
  const results: { id: string; title: string; text: string; type: 'intro' | 'meeting' }[] = [];

  introSections.forEach(s => {
    results.push({ id: s.id, title: s.title, text: s.content.join(' '), type: 'intro' });
  });

  meetings.forEach(m => {
    const allText = [
      m.title, m.subtitle,
      ...m.objectives,
      ...m.content,
      ...m.activities.flatMap(a => [a.title, ...a.description]),
      ...m.closingNotes
    ].join(' ');
    results.push({ id: m.id, title: `बैठक ${m.meetingNumber}: ${m.title}`, text: allText, type: 'meeting' });
  });

  return results;
}
